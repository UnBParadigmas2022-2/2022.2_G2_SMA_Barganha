
public class caracteristicas {
	private int price;
	private String quality;
	
	// Constructor
	public caracteristicas(int price, String quality) {
		this.price = price;
		this.quality = quality;
	}
	
	// get price
	public int getPrice() {
		return price;
	}		
	
	// get quality
	public String getQuality() {
		return quality;
	}
}
